/**
 * Save detected gaps to the database.
 */
export async function saveGaps(
  pool: pg.Pool,
  gaps: Array<{ representativeQuestion: string; sampleQueries: string[]; clusterSize: number; priority: string }>
): Promise<number> {
  let saved = 0;
  for (const gap of gaps) {
    // Check if similar gap already exists (avoid duplicates)
    const existing = await pool.query(
      `SELECT id FROM faq_gaps
       WHERE representative_question = $1 AND status = 'pending'`,
      [gap.representativeQuestion]
    );

    if (existing.rows.length === 0) {
      await pool.query(
        `INSERT INTO faq_gaps (representative_question, sample_queries, cluster_size, priority)
         VALUES ($1, $2, $3, $4)`,
        [gap.representativeQuestion, gap.sampleQueries, gap.clusterSize, gap.priority]
      );
      saved++;
    }
  }
  return saved;
}
